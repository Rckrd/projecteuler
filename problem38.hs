                         (a*10+b)/(x*10+y) == (a/y) && b == x ||
                         (a*10+b)/(x*10+y) == (a/x) && b == y||
                         (a*10+b)/(x*10+y) == (b/x) && a == y||
                         (a*10+b)/(x*10+y) == (b/y) && a == x]
